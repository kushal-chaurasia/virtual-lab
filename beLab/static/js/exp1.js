let check1 = document.getElementById('check1')
check1.addEventListener("click", function (e) {
    let ques1 = document.getElementById('ques1').value;
    let unit1 = document.getElementById('unit1').value;
    let tol1 = document.getElementById('tol1').value;
    let answer1 = document.getElementById('answer1')

    if (ques1 == "300") {
        answer1.innerHTML += `Correct resistance value <br>`

    }
    else {
        answer1.innerHTML += `Incorrect resistance value <br>`
    }
    if (unit1 == 'kohm') {
        answer1.innerHTML += `
         Correct Unit Value
        `

    } else {
        answer1.innerHTML += `
         Incorrect Unit Value
        `

    }
    if (tol1 == '5') {
        answer1.innerHTML += `
         Correct Tolerance Value <br>
        `
    }
    else {
        answer1.innerHTML += `
         Incorrect Tolerance Value <br>
        `
    }

});

